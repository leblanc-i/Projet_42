#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	i;
	size_t	j;
	size_t	str_len;
	char	*str;

	if (!s1 || !s2)
		return (NULL);
	i = 0;
	str_len = ft_strlen((char *)s1) + ft_strlen((char *)s2);
	str = malloc((str_len + 1) * sizeof(char));
	if (!str)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		str[i + j] = s2[j];
		j++;
	}
	str[i + j] = '\0';
	return (str);
}
